package operators;

public class Unary {

	public static void main(String[] args) {
		int i=10;
		
		System.out.println(i++);
		System.out.println(i--);
		System.out.println(++i);
		System.out.println(i++);
		System.out.println(i++);
		System.out.println(i++ + ++i + --i - i++);

	}




	}


